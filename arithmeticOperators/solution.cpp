#include <bits/stdc++.h>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;

    cout << "Sum: " << a + b << "\n";   
    cout << "Difference: " << a - b << "\n";   
    cout << "Product: " << a * b << "\n";
    cout << "Division: " << a / b << "\n";
    cout << "Remainder: " << a % b << "\n";
    return 0;
}